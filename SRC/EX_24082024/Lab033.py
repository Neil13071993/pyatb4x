# Create a program to sum of three number from the user input

num1 = int(input("Enter the value of num1\n"))
num2 = int(input("Enter the value of num2\n"))
num3 = int(input("Enter the value of num3\n"))

def sum_of_the_three_numbers(num1, num2, num3):
    return num1 + num2 + num3
result = sum_of_the_three_numbers(num1, num2, num3)
print(result)
